package model;

public class UnitStatistics {
    private int totalUnits;
    private int totalStorage;
    private int totalTransport;
    private int totalApproved;
    private int totalPending;
    private int totalRejected;

    // Getters and Setters

    public int getTotalUnits() {
        return totalUnits;
    }

    public void setTotalUnits(int totalUnits) {
        this.totalUnits = totalUnits;
    }

    public int getTotalStorage() {
        return totalStorage;
    }

    public void setTotalStorage(int totalStorage) {
        this.totalStorage = totalStorage;
    }

    public int getTotalTransport() {
        return totalTransport;
    }

    public void setTotalTransport(int totalTransport) {
        this.totalTransport = totalTransport;
    }

    public int getTotalApproved() {
        return totalApproved;
    }

    public void setTotalApproved(int totalApproved) {
        this.totalApproved = totalApproved;
    }

    public int getTotalPending() {
        return totalPending;
    }

    public void setTotalPending(int totalPending) {
        this.totalPending = totalPending;
    }

    public int getTotalRejected() {
        return totalRejected;
    }

    public void setTotalRejected(int totalRejected) {
        this.totalRejected = totalRejected;
    }
    
}
